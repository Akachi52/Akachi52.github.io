function toggle(){
    var x =  document.getElementById("hold");

    if(x.style.display=== "none"){
        x.style.display = "block";
    }
    else{
        x.style.display = "none";
    }
}
function removeHold(){
    var y = document.getElementById("hold");
    y.style.display = "none"
}
